        <h1>The Scottish Court of Session Papers</h1>
        <p>The case papers of the Scottish Court of Session (Scotland’s supreme civil court) are perhaps the most significant – yet still unstudied –
           printed source for the history, society and literature of Scotland from 1710-1850. During this period, every paper that came before the
           Court was required to be printed for the lawyers, in a small number of copies.
        </p>
        <p>There are three surviving large collections of the Session Papers, held by Edinburgh University Library,
           the Faculty of Advocates, and the Signet Library, all in the city of Edinburgh and containing approximately 250,000 items.
        </p>
        <p>The collections in Edinburgh include the written pleadings of contested cases, answers, replies, and case summaries,
           many of which have contemporary annotations. Supplemental case materials appended to these documents include maps, building plans,
           and printed copies of correspondence, wills, financial accounts, and census reports.
        </p>
        <p>This is almost certainly the world’s largest single body of uncatalogued English language printed material before 1900,
           and the most valuable unstudied source for Scottish history in existence.
        </p>
        <p>From October 2016 to April 2017 – and in partnership with the Faculty of Advocates and the Signet Library –
           the University led a pilot project which captured 13,500 images from the three collections.
           A follow-up project is now underway and more images will be added here later in 2018
        </p>

