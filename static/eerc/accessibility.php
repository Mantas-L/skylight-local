        <h3>Content that's not within the scope of the accessibility regulations</h3>
        <p><strong>Interactive maps</strong></p>
        <p>This website hosts a mapping service. Currently, online mapping and mapping services sit outwith the 2018 accessibility regulations.</p>
        <p>“These Regulations do not apply to the following content of a website or mobile application of a public sector body—
            (d)online maps and mapping services, as long as essential information is provided in an accessible digital manner for maps intended for navigational use;”  (The Public Sector Bodies (Websites and Mobile Applications) (No. 2) Accessibility Regulations 2018)”
        </p>
        <p>The mapping service is not fully accessible, as it is not possible to interact with individual map features using assistive technologies. Although maps are currently not within the scope of the regulations, we will continue to strive to make the site as accessible as possible.</p>
        <p>There is additional information available in different formats, including maps, to download and information in the AccessAble - UoE app available for iOS and Android:</p>
        <p><a href="https://www.ed.ac.uk/estates/buildings-information/disability">AccessAble Information</a>.</p>
        <p><strong>PDFs</strong></p>
        <p>Many of our older PDFs do not meet accessibility standards – for example, they may not be structured so they are accessible to a screen reader. This does not meet WCAG 2.1 success criterion 4.1.2 (name, role, value).</p>
        <p><a href="https://www.w3.org/TR/WCAG21/#name-role-value">WCAG 2.1 success criterion 4.1.2 (name, role, value)</a></p>
        <p>Some of our PDFs are essential to accessing information on the site. For example, several of the audio interview files have corresponding transcripts in PDF form. We will try to ensure any new PDFs or Word documents we publish will meet accessibility standards.</p>
        <p><a href="https://www.legislation.gov.uk/uksi/2018/952/regulation/4/made">Regulations for PDF's</a></p>

        <h2>What we're doing to improve accessibility</h2>
        <p>At this time, we believe all items are within our control. We will continue to address the accessibility issues highlighted to deliver a solution or suitable workaround. We are looking to move this site to a new content management system within the next 12 months and will be working to ensure this resolves the accessibility issues. Unless specified otherwise, a complete solution or significant improvement will be in place for those items within our control by March 2024.</p>
        <p>While we are in the process of resolving these accessibility issues, or where we are unable, we will ensure reasonable adjustments are in place to make sure no user is disadvantaged. As changes are made, we will continue to review accessibility and retest the accessibility of this website.</p>

        <h2>Preparation of this accessibility statement</h3>
        <p><strong>This statement was prepared on10th September 2020. It was last reviewed on 3rd March 2023.</strong></p>
        <p>This website was first tested by the Digital Library & Development team in September 2020 and reviewed on 15th September 2021 and again on 3rd March 2023. The testing was initially carried out by The University of Edinburgh Library and University Collections Digital Library Development team using the automated <a href="https://wave.webaim.org/">Wave WEBAIM</a> and <a href="https://littleforest.co.uk/">Little Forest</a> testing tool. The testing in October 2022 included full manual as well as automated testing.</p>
        <p>This website was last manually tested by the Digital Library team, Library and University Collections, University of Edinburgh in March 2023. This was primarily using Mozilla Firefox (91.7.1esr), Microsoft Edge (99.0.1150.55) and Google Chrome (99.0.4844.84), browsers for comparative purposes.</p>
        <p>Recent world-wide usage levels survey for different screen readers and browsers shows that Chrome, Mozilla Firefox and Microsoft Edge are increasing in popularity and Google Chrome is now the favoured browser for screen readers:</p>
        <p><a href="https://webaim.org/projects/screenreadersurvey9/">WebAIM: Screen Reader User Survey</a></p>
        <p>The aforementioned three browsers have been used in certain questions for reasons of breadth and variety.</p>
        <p>We ran automated testing using <a href="https://wave.webaim.org/">Wave WEBAIM</a> and <a href="https://littleforest.co.uk/">Little Forest</a> then manual testing that included:</p>
        <ul>
            <li>Spell check functionality;</li>
            <li>Scaling using different resolutions and reflow;</li>
            <li>Options to customise the interface (magnification, font, background colour, etc);</li>
            <li>Keyboard navigation and keyboard traps;</li>
            <li>Data validation;</li>
            <li>Warning of links opening in a new tab or window;</li>
            <li>Information conveyed in colour or sound only;</li>
            <li>Flashing, moving or scrolling text;</li>
            <li>Operability if JavaScript is disabled;</li>
            <li>Use with screen reading software (for example, JAWS);</li>
            <li>Assistive software (TextHelp Read and Write, Windows Magnifier, ZoomText, Dragon NaturallySpeaking, TalkBack and VoiceOver);</li>
            <li>Tooltips and text alternatives for any non-text content;</li>
            <li>Time limits;</li>
            <li>Compatibility with mobile accessibility functionality (Android and iOS).</li>
        </ul>

        <h2>Change Log</h2>
