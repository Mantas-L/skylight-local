              <p>
                  The World History of Rashid al-Din, 1314: A Masterpiece of Islamic Painting, a major exhibition at
                  the Main Library, University of Edinburgh in collaboration with Centre for Research Collections and
                  the Alwaleed Bin Talal Centre for the Study of Islam in the
                  Contemporary World.
              </p>
              <p>
                  Curated by the Prince Alwaleed Centre and the Centre for Research Collections, the exhibition featured
                  folios from the Jami’ al-Tawarikh (or “World History”) of Rashid al-Din: one of the most important
                  illustrated medieval manuscripts to have survived from either East or West. Datable to 1314, it was
                  produced in the city of Tabriz, a seat of power of the Ilkhanid rulers, descendants of the Mongol Chingiz
                  Khan, who held sway over an empire encompassing Persia and large parts of present-day Azerbaijan and Turkey.
