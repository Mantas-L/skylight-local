        <h1>About the University Collections</h1>
        <p>
            Special Collections include manuscripts and archives, rare books, photographs, printed and handwritten music,
            architectural drawings, theses and microfilms. The collections relate to University of Edinburgh and its history,
            and to Scotland and the wider world.
        </p>
        <p>
            In total we have about 35 kilometres of historic material, much of which cannot be found anywhere else in the world.
        </p>
        <h1>University Collections Iconics Online</h1>
        <p>
           This collection represents choices made by the collection curators within the Centre of Research Collections.
           These iconic items are the most beautiful, important and unique treasures in the University’s collections.
            Some, like the Rashid al-Din manuscript, are known across the world. Others, like Clement Litill’s charter
            which founded the Library, are key to the story of our collections.
        </p>
