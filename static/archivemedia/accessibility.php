<h2 class="western"><font face="Arial, serif"><font size="5" style="font-size: 17pt">How
accessible this website is</font></font></h2>
<p style="line-height: 100%; margin: 0cm"></p>
<p style="line-height: 100%; margin-bottom: 0cm"><font face="Arial, serif">We
know some parts of this website are not fully accessible:</font></p>
<p style="line-height: 100%; margin: 0cm"></p>
<ul>
	<li><p style="line-height: 100%; margin-bottom: 0cm"><font face="Times New Roman, serif"><font face="Arial, serif">Some
	colour contrasts do not meet WCAG 2.2 standards</font></font></p></li>
	<li><p style="line-height: 100%; margin-bottom: 0cm"><font face="Times New Roman, serif"><font face="Arial, serif">Reflow
	is not enabled to 400%</font></font></p></li>
	<li><p style="line-height: 115%; margin-bottom: 0.35cm"><font face="Arial, serif">Some
	selection displays on the website lack visibility meaning user may
	struggle to see where they have navigated to on the site</font></p></li>
	<li><p style="line-height: 115%; margin-bottom: 0.35cm"><font face="Arial, serif">Keyboard
	navigation does not follow a logical order</font></p></li>
	<li><p style="line-height: 115%; margin-bottom: 0.35cm"><font face="Arial, serif">There
	is no “Skip to main content” link or button</font></p></li>
	<li><p style="line-height: 115%; margin-bottom: 0.35cm"><font face="Arial, serif">Screen
	readers are not fully compatible with the site</font></p></li>
	<li><p style="line-height: 115%; margin-bottom: 0.35cm"><font face="Arial, serif">Tooltips
	cannot be reached by keyboard navigation or assistive technology</font></p></li>
	<li><p style="line-height: 115%; margin-bottom: 0.35cm"><font face="Arial, serif">Voice
	recognition software is not fully compatible with the site</font></p></li>
	<li><p style="line-height: 115%; margin-bottom: 0.35cm"><font face="Arial, serif">Some
	hyperlinks open new tabs/windows without warning the user</font></p></li>
	<li><p style="line-height: 115%; margin-bottom: 0.35cm"><font face="Arial, serif">Missing
	spellcheck functionality for all free text boxes</font></p></li>
	<li><p style="line-height: 115%; margin-bottom: 0.35cm"><font face="Arial, serif">Transcriptions,
	captions, and audio descriptions were all missing from audio content</font></p></li>
</ul>
