        <h1>About the University of Edinburgh Calendar Collection</h1>
        <p>Calendars are produced every year to showcase the diverse range of material held in the University's collections. These are generally produced around a specific theme, e.g. Bygone Edinburgh (2014), Historic Musical Instruments (2012) and Travel (2008)  but in some years (e.g. 2010 and 2011) a selection of images illustrating Treasures of the Collections are selected.</p>
        <p>All images were created by the Digital Imaging Unit and are copyright the University of Edinburgh.<p/>
        <p>Images from all calendars, from 2006 onwards will be available in this collection.<p/>
        <ul>
            <li>2006 - Treasures from the Collection</li>
            <li>2007 - The Natural World</li>
            <li>2008 - Travel</li>
            <li>2009 - no calendar</li>
            <li>2010 - Treasures from Special Collections</li>
            <li>2011 - Treasures from University Collections</li>
            <li>2012 - Historic Musical Instruments at St. Cecilia's Hall</li>
            <li>2013 - Japanese Paintings of Children's Games by Kobayashi Eitaku</li>
            <li>2014 - Bygone Edinburgh</li>
        </ul>

        </p>
        <h1>University Collections</h1>
        <p>
            Special Collections include manuscripts and archives, rare books, photographs, printed and handwritten music, architectural drawings, theses and microfilms. The collections relate to University of Edinburgh and its history, and to Scotland and the wider world.</p>
        <p>In total we have about 35 kilometres of historic material, much of which cannot be found anywhere else in the world. <a href="http://collections.ed.ac.uk" Title="University Collections Home">Collections.ed.ac.uk</a>
        </p>

